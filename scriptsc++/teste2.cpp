#include<iostream>
using namespace std;
int main(void){
    /* Para criar uma variável basta colocar o tipo da variável seguido do seu nome e um valor*/
    int numero = 10;
    /* Para criar um número flutuante use o float*/
    float pi = 3.14;
    /* Para números muito grandes use o double*/
    double numero_grande = 987665543312414.87867676565454434535789;
    /* Para uma única letra usa-se o char*/
    char letra = 'a';
    cout<<"O numero e "<<numero<<endl;
    cout<< "Pi e "<<pi<<endl;
    cout<< "O numero gigante e "<<numero_grande<<endl;
    cout<< "A letra e "<<letra<<endl;
    return 0;
}